<h2 style="text-decoration:underline">Objective of E-Guru</h2>
<p>
"Development of Personalised and Performance based
E- Learning tool for existing E-resources." is a joint project with collaboration of <b>NIT DURGAPUR, IIT KANPUR </b>and <b>BANNARI AMMAN INSTITUTE OF TECHNOLOGY ALATHUKOMBAI</b> and funded  by <b>DeitY, Government of India</b>.
</p>
<p>The rapid advancement in Information and Communication Technologies (ICT),
availability of quality infrastructure and cheap services have led to exponential
expansion of applications of ICT in almost all the fields of endeavor. The world of
education has also been greatly benefited by advancement in ICT and its uses. ICT has
enabled a variety of new avenues and methodologies for enhancing the experience of 
learning and teaching. The traditional teaching and learning have some limitations
associated with them in achieving their objectives efficiently and economically. Efforts
are being made around the world to overcome these limitations by using Information
Communication Technology.</p>
<p>ICT enabled large number of learning resources is
available and the use of the learning resources have been accelerated in many fold due to
the rapid growth of internet and its easy as well as affordable accessibility. The resources
are also becoming easily accessible and affordable among all section of people through
mobile technology.To promote the e-learning among learning and teaching community, Government of
India has started many projects by under Ministry of Communication and Information
Technology, Ministry of Human Resources and Development, Government of India
under the mission called ―National Mission on Education through Information and
Communication Technologies.
<p>As examples the web portals of NPTEL and NMPE-ICT are enabling to act as the
catalyst for e-learning resources in higher education. The contents are developed in
NPTEL and NMPE by experienced teachers in different fields of education and the user       
agency includes all higher education institutions, universities as well as industries.
However, it is observed that there is a gap between delivering the resources and learning
the same at user level and it is to be addressed wisely to implement the projects of e
learning resources in efficient manner. It is also observed that the e-learning resources
like NPTEL, NMPE, virtual laboratories and ICT enabled laboratories need an efficient
mechanism for better and effective utilization. In this project, eGuru is proposed for
better and effective utilization of all e-learning resources like NPTEL, NMPE, virtual
laboratories and ICT enabled remote laboratories etc.</p>
The e-Guru is an agent that provides personalized recommendations to the students
depending on their profile and performance in e-learning resources for their opted
lessons either in theoretical courses or laboratory courses. One of the most desired
characteristics of e-learning systems is that of being personalized, since they have to be
used by a wide variety of students with different skills and pre subject knowledge.
Different students learn in different ways and different environments. The basic
parameters for the personalization of e-learning systems based on the learning
preferences, skill, pre subject knowledge and environment etc. It is also observed that
some of the student process information reflectively while others actively. Some students
prefer abstract material while others prefer concrete examples with visual presentations.
Some study steadily while others in fits and starts. Thus, e-learning systems will be
effective where the parameters like each student‘s learning preferences and skills as
mentioned above will be given the priority to formulate any alternative teaching and
learning systems.</p>

<p>To achieve its goal, e-Guru is build with the student profile, based on the
student‘s learning style which depends on their capability indexes like receiving and
processing information available in e-learning resources. The proposed agent, e-Guru,
detects a student‘s learning style.</p>
