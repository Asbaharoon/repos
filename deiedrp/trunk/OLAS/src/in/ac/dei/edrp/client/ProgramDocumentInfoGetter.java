package in.ac.dei.edrp.client;

import com.google.gwt.user.client.rpc.IsSerializable;
/**
 * This bean file is associated with Program Document
 * @author UPASANA KULSHRESTHA
 * @date 10 February 2012
 * @version 1.0
 */
public class ProgramDocumentInfoGetter implements IsSerializable{
	/** declaring private variables */
	private String universityId;
	private String programId;
	private String docId;
	private String userId;
	private int countprogramId;
	private String groupCode;
	private String docDesc;
	
	/** defining setter and getter method for private variable **/
	public String getUniversityId() {
		return universityId;
	}
	public void setUniversityId(String universityId) {
		this.universityId = universityId;
	}
	public String getProgramId() {
		return programId;
	}
	public void setProgramId(String programId) {
		this.programId = programId;
	}
	public String getDocId() {
		return docId;
	}
	public void setDocId(String docId) {
		this.docId = docId;
	}
	public int getCountprogramId() {
		return countprogramId;
	}
	public void setCountprogramId(int countprogramId) {
		this.countprogramId = countprogramId;
	}
	public String getGroupCode() {
		return groupCode;
	}
	public void setGroupCode(String groupCode) {
		this.groupCode = groupCode;
	}
	public String getDocDesc() {
		return docDesc;
	}
	public void setDocDesc(String docDesc) {
		this.docDesc = docDesc;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	
	
}
