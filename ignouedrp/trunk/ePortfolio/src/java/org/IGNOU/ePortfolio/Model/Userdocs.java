package org.IGNOU.ePortfolio.Model;

import java.io.Serializable;

/**
 *
 * @author IGNOU Team
 */
public class Userdocs implements Serializable {

    private String user_id;
    private long fileid;
    private Long size;
    private String filetype;
    private String filename;
    private String filepath, description, filedate;

    public Userdocs(String user_id, long fileid, Long size, String filetype, String filename, String filepath, String description, String filedate) {
        this.user_id = user_id;
        this.fileid = fileid;
        this.size = size;
        this.filetype = filetype;
        this.filename = filename;
        this.filepath = filepath;
        this.description = description;
        this.filedate = filedate;
    }

    public Userdocs() {
    }

    /**
     * @return the user_id
     */
    public String getUser_id() {
        return user_id;
    }

    /**
     * @param user_id the user_id to set
     */
    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }

    /**
     * @return the fileid
     */
    public long getFileid() {
        return fileid;
    }

    /**
     * @param fileid the fileid to set
     */
    public void setFileid(long fileid) {
        this.fileid = fileid;
    }

    /**
     * @return the size
     */
    public Long getSize() {
        return size;
    }

    /**
     * @param size the size to set
     */
    public void setSize(Long size) {
        this.size = size;
    }

    /**
     * @return the filetype
     */
    public String getFiletype() {
        return filetype;
    }

    /**
     * @param filetype the filetype to set
     */
    public void setFiletype(String filetype) {
        this.filetype = filetype;
    }

    /**
     * @return the filename
     */
    public String getFilename() {
        return filename;
    }

    /**
     * @param filename the filename to set
     */
    public void setFilename(String filename) {
        this.filename = filename;
    }

    /**
     * @return the filepath
     */
    public String getFilepath() {
        return filepath;
    }

    /**
     * @param filepath the filepath to set
     */
    public void setFilepath(String filepath) {
        this.filepath = filepath;
    }

    /**
     * @return the description
     */
    public String getDescription() {
        return description;
    }

    /**
     * @param description the description to set
     */
    public void setDescription(String description) {
        this.description = description;
    }

    /**
     * @return the filedate
     */
    public String getFiledate() {
        return filedate;
    }

    /**
     * @param filedate the filedate to set
     */
    public void setFiledate(String filedate) {
        this.filedate = filedate;
    }
}
